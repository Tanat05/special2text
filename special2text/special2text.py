def _flatten_replacements(groups: Iterable[Iterable[tuple[str, str]]]) -> list[tuple[str, str]]:
    flattened: list[tuple[str, str]] = []
    for group in groups:
        flattened.extend(group)
    flattened.extend(ASCII_DIGIT_MAP)
    flattened.sort(key=lambda item: len(item[0]), reverse=True)
    return flattened


def _apply_replacements(text: str, replacements: Iterable[tuple[str, str]]) -> str:
    for source, target in replacements:
        text = text.replace(source, target)
    return text


def _strip_combining_marks(text: str) -> str:
    decomposed = unicodedata.normalize("NFKD", text)
    return "".join(char for char in decomposed if unicodedata.category(char) not in COMBINING_MARK_CATEGORIES)


def _keep_character_in_korean_cleanup(char: str) -> bool:
    if char.isspace():
        return True
    if char.isdigit() or char.isascii() and char.isalpha():
        return True
    if char in KEEP_PUNCTUATION:
        return True

    codepoint = ord(char)
    return (
        0xAC00 <= codepoint <= 0xD7A3
        or 0x1100 <= codepoint <= 0x11FF
        or 0x3130 <= codepoint <= 0x318F
        or 0xA960 <= codepoint <= 0xA97F
        or 0xD7B0 <= codepoint <= 0xD7FF
    )


def _cleanup_non_korean_letters(text: str) -> str:
    return "".join(char for char in text if _keep_character_in_korean_cleanup(char))


def _normalize_unicode_digits(text: str, drop_non_ascii_digits: bool = False) -> str:
    normalized_characters = []
    for char in text:
        if char.isdigit():
            if drop_non_ascii_digits and not char.isascii():
                continue
            try:
                normalized_characters.append(str(unicodedata.digit(char)))
                continue
            except (TypeError, ValueError):
                pass
        normalized_characters.append(char)
    return "".join(normalized_characters)


