def _map_mathematical_alphanumerics(text: str) -> str:
    """Map characters from the Mathematical Alphanumeric Symbols block
    (and similar named characters) back to plain ASCII letters/digits.
    This handles characters whose Unicode name contains 'MATHEMATICAL'.
    """
    out_chars: list[str] = []
    for ch in text:
        try:
            name = unicodedata.name(ch)
        except ValueError:
            out_chars.append(ch)
            continue

        if "MATHEMATICAL" in name:
            tokens = name.split()
            # handle digits like 'MATHEMATICAL BOLD DIGIT THREE'
            if "DIGIT" in tokens:
                word = tokens[-1]
                mapped = _WORD_TO_DIGIT.get(word, None)
                if mapped is not None:
                    out_chars.append(mapped)
                    continue
            # handle letters — check for CAPITAL/SMALL to preserve case
            letter = tokens[-1]
            if len(letter) == 1 and letter.isalpha():
                if "SMALL" in tokens:
                    out_chars.append(letter.lower())
                else:
                    out_chars.append(letter.upper())
                continue

        out_chars.append(ch)
    return "".join(out_chars)


def special_text(text: str, preferred_language: str = "en", cleanup_non_korean: bool = False) -> str:
    """
    Convert stylized and multilingual special characters to plain text.

    preferred_language accepts:
    - "en": prioritize Latin-style normalization
    - "ko": prioritize Korean-specific replacements first

    cleanup_non_korean removes unsupported foreign-script letters after
    normalization, which is useful for heavily decorated Korean text.
    """
    normalized_language = preferred_language.lower().strip()
    if normalized_language not in REPLACEMENT_ORDER:
        normalized_language = "en"

    if normalized_language == "ko":
        text = _apply_replacements(text, _flatten_replacements((KOREAN_PRE_REPLACEMENTS,)))

    text = _strip_combining_marks(text)
    text = unicodedata.normalize("NFKC", text)
    text = _map_mathematical_alphanumerics(text)
    text = _normalize_unicode_digits(text, drop_non_ascii_digits=normalized_language == "ko" and cleanup_non_korean)
    replacements = _flatten_replacements(REPLACEMENT_ORDER[normalized_language])
    text = _apply_replacements(text, replacements)
    text = _compose_hangul(text)
    if normalized_language == "ko" and cleanup_non_korean:
        text = _cleanup_non_korean_letters(text)
    return text


def special_en(text: str) -> str:
    """Backward-compatible English-first alias."""
    return special_text(text, preferred_language="en")


def special_ko(text: str, cleanup_non_korean: bool = True) -> str:
    """Korean-first normalization alias."""
    return special_text(text, preferred_language="ko", cleanup_non_korean=cleanup_non_korean)


