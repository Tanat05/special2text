    def main() -> None:
        try:
            text = input("Enter text: ")
        except EOFError:
            return
        preferred_language = input("Preferred language priority (en/ko, default en): ").strip().lower() or "en"
        cleanup_choice = input("Remove unsupported non-Korean letters? (y/N): ").strip().lower() == "y"
        print(special_text(text, preferred_language=preferred_language, cleanup_non_korean=cleanup_choice))

    main()